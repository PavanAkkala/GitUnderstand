package org.example;

public class MidValueBetweenTwoNums {

    public static void main(String[] args) {

        int start = 1;
        int end = 11;

        // Calculate the midpoint
        int mid = (start + end) / 2;

        // Print the result
        System.out.println("The mid value between " + start + " and " + end + " is: " + mid);
    }

}
